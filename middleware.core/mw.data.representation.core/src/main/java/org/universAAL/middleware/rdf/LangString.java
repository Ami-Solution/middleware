    /** The String. */
    private String str = "";

    /** The language tag of the string. */
    private String lang = "";

    /**
     * Create a new String with language tag.
     *
     * @param str
     *            The String.
     * @param lang
     *            The language tag of the string.
     */
    public LangString(String str, String lang) {
	if (str == null)
	    this.str = "";
	else
	    this.str = str;

	if (lang == null)
	    this.lang = "";
	else
	    this.lang = lang;
    }

    /**
     * Get the String.
     *
     * @return the String.
     */
    public String getString() {
	return str;
    }

    /**
     * Get the language tag of the string.
     *
     * @return The language tag of the string.
     */
    public String getLang() {
	return lang;
    }

    @Override
    public boolean equals(Object obj) {
	if (!(obj instanceof LangString)) {
	    if (obj instanceof String && lang.isEmpty())
		return true;
	    return false;
	}
	LangString l = (LangString) obj;
	if (str.equals(l.str) && lang.equals(l.lang))
	    return true;
	return false;
    }

